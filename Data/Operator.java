public class Operator {
    public static void main(String[] args) {
        System.out.println(5<<2);  // left shift=val*2pow(2)
        System.out.println(20>>2); //right shift-> val/2pow(2)


        System.out.println(5>>2);  
        System.out.println(20<<2);

        System.out.println(20>>>2);
        System.out.println(-20>>>2);
    }
}
